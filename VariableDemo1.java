public class VariableDemo1 {
    public static void main(String[] args) {
        byte x;   // 定义一个字节类型变量, 占8位
        short s;  // 定义一个短整型变量, 占16位
        int i;    // 定义一个整型变量,占32位
        long l;   // 定义一个长整型变量,占64位
        float f;  // 定义一个单精度浮点型变量,占32位
        double d; // 定义双精度浮点型变量,占64位
        char c;   // 定义一个字符类型变量,占16位
        boolean b;// 定义一个布尔类型变量,占1位
    }
}
