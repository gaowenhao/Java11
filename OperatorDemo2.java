public class OperatorDemo2 {
    public static void main(String[] args) {
        var a = 3;
        var b = 4;

        System.out.println(a == b);  // 判断a,b是否相等   true
        System.out.println(a > b);   // 判断a是否大于b    false
        System.out.println(a < b);   // 判断a是否小于b    true
        System.out.println(a >= b);  // 判断a是否大于或等于b  false
        System.out.println(a <= b);  // 判断a是否小于或等于b  true
        System.out.println(a != b);  // 判断a,b是否不相等    true
    }
}
