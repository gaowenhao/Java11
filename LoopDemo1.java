public class LoopDemo1 {
    public static void main(String[] args) {
        for (var i = 0; i < 10; i++) {          //for是关键字，后面括号里分别是(初始定义;是否继续循环;每次循环后要执行的代码.)
            System.out.println("Hello World");
        }
    }
}
