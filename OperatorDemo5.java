public class OperatorDemo5 {
    public static void main(String[] args){
        var x = 10;
        x += 20;  // 相当于运算流程是: x = x + 20
        x *= 2;    // 相当于x = x * 2
        x <<= 1; // 相当于x = x << 1
    }
}
