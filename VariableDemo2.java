public class VariableDemo2 {
    public static void main(String[] args) {
        int a;                         // 定义
        a = 10;                        // 赋值
        int b, c;                      // 定义多个变量
        b = 10;                        // 赋值
        c = 15;                        // 赋值

        int d = 10;                    // 定义并且赋值
        int x = 10, y = 15, z = 200;

        float f = 10.5f;               // 定义一个浮点型变量
        char ch = 'A';                 // 定义一个字符变量
        boolean bool = true;           // 定义一个boolean变量

        System.out.print(ch);
    }
}
